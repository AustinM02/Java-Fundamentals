package Chapter2.Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        double operand1 = 7;
        double operand2 = 12;

        System.out.println("1 - " + (operand1 == operand2));
        System.out.println("2 - " + (operand1 != operand2));
        System.out.println("2 - " + (operand1 > operand2));
        System.out.println("2 - " + (operand1 < operand2));
        System.out.println("2 - " + (operand1 >= operand2));
        System.out.println("2 - " + (operand1 <= operand2));

    }
}
